  <h1>Uwagi</h1>
  <p>Nazwy plików mają być dokładnie jak podano niżej.
  Jeżeli w domu posiadasz starszą wersję Office’a to zamiast rozszerzenia .docx, zapisz plik z rozszerzeniem .doc.
  Jeżeli posiadasz pakiet OpenOffice to rozszerzeniem pliku będzie .odt.</p>
  <p>Informatyka to przedmiot ścisły. Bądź precyzyjny, każda spacja, kropka, literka jest ważnym znakiem, więc nazwy wpisuj dokładnie jak polecono!</p>

  <h1>Przygotowanie (<span>5 punktów</span>)</h1>
  <p>Utwórz nowy plik edytora tekstu i nazwij go następująco:<br />
  jeżeli używasz Microsoft Word 2007 lub nowszego: <code>1x_Nazwisko_Imie_nauka_online.docx</code>;<br />
  jeżeli używasz Open Office lub LibreOffice: <code>1x_Nazwisko_Imie_nauka_online.odt</code><br />
  gdzie w miejsce <code>x</code> wpisz symbol klasy (a,b,c,d lub e) a zamiast <code>Nazwisko_Imie</code>
  wpisz swoje nazwisko i imię (bez zdrobnień). Zwróć uwagę kropki, spacje i inne znaki w nazwie.</p>

  <h1>Bezpieczeństwo pracy (<span>6 punktów</span>)</h1>
  <p>W pliku podaj przynajmniej cztery czynności, które warto wykonywać aby zabezpieczyć system operacyjny oraz dane.
    Skorzystaj z podręcznika (rozdział 1) lub z informacji, które znajdziesz na stronach internetowych.
    Napisz <strong>zwięźle na temat</strong>, dłuższe opisy nie będą oceniane lub stracisz punkty za niepotrzebne opisy.</p>

  <h1>E-learning (<span>6 punktów</span>)</h1>
  <p>Następnie w tym samym pliku napisz co najmniej trzy zalety i trzy wady nauki na odległość czyli e-learningu.
    Skorzystaj z podręcznika (rozdział 20) lub z informacji, które znajdziesz na stronach internetowych (na przykład <a href="https://informatyka.wsip.pl/informatyka-1/20-uczestnictwo-w-kursie-e-learningowym/">tutaj</a>).
    Tak jak powyżej pisz krótko (dłuższe opisy nie oznaczają szansy na wyższą ocenę).
    <br><em><strong>Pamiętaj w obu przypadkach, aby podać źródła z których korzystałeś(-aś)!</strong></em>.</p>

  <h1>Konfiguracja poczty i wysyłanie wiadomości (<span>9 punktów</span>)</h1>
  <p>Jeżeli nie zrobiłeś(-aś) tego na lekcji: skonfiguruj własną pocztę e-mail w taki sposób,
    aby w polu <em>OD</em> pojawiało się Twoje imię i nazwisko. <span>(3 punkty)</span></p>
  <ol>
    <li>Utwórz nową wiadomość e-mail.</li>
    <li>Wpisz temat wiadomości (w miejsce x wpisz swoją klasę, czyli a,b,c,d lub e) <code>1x nauka online</code>. <span>(3 punkty)</span></li>
    <li>Dołącz do niej plik utworzony w powyższych poleceniach (czyli <code>1x_Nazwisko_Imie_nauka_online.docx</code>).</li>
    <li>Wyślij wiadomość na adres <code>bartek_tr@tlen.pl</code></li>
    <li>Jeżeli korzystasz z narzędzia Classroom: zapisz ten plik na dysku zajęć oraz udostępnij go nauczycielowi
      (czyli dla adresu <code>bartek.trojnar.430@gmail.com</code>. <span>(3 punkty)</span>
      <br />W przeciwnym wypadku przez komunikator w dzienniku napisz wiadomość z tematem <code>online</code>: "nie korzystam z Classroom".</li>
  </ol>

