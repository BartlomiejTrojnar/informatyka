<h1>Kryteria oceniania - ocena końcowa</h1>
<ol>
  <li>Ocena końcowa na zajęciach informatyki jest wyznaczona przez poszczególne oceny i ich wagę – średnią ważoną wszystkich uzyskanych przez ucznia ocen.</li>
  <li>Uczeń, który w ciągu roku uzyska średnią:
    <ol type="a">
      <li>nie większą niż 1,4 uzyskuje ocenę niedostateczną na koniec roku;</li>
      <li>większą niż 1,75, a mniejszą niż 2,4 – uzyskuje ocenę dopuszczającą;</li>
      <li>większą niż 2,6, a mniejszą niż 3,4 – uzyskuje ocenę dostateczny;</li>
      <li>większą niż 3,6, a mniejszą niż 4,4 – uzyskuje ocenę dobry;</li>
      <li>większą niż 4,6 a mniejszą niż 5,1 – uzyskuje ocenę bardzo dobry;</li>
      <li>większą niż 5,5 – uzyskuje ocenę celujący.</li>
    </ol>
  </li>
  <li>W przypadku jeżeli wyżej wymieniona średnia mieści się w zakresach niewymienionych powyżej do wyznaczenia oceny rocznej będą brane pod uwagę również:
    <ul>
      <li>postawa ucznia na lekcjach,</li>
      <li>wykonywanie zadań w terminie,</li>
      <li>obecności na lekcjach,</li>
      <li>lub inne aspekty.</li>
    </ul>
    Np. jeżeli uczeń uzyska średnią ocen między 2,4 a 2,6 – uzyska ocenę dopuszczającą lub dostateczną na koniec roku.</li>
</ol>

<h1>Wymagania wobec ucznia</h1>
<ol>
  <li>Uczeń nie musi posiadać zeszytu ani podręcznika.</li>
  <li><em style="color: #d50; font-weight: bold;">Uczeń w czasie lekcji powinien korzystać z przygotowanego dla niego konta, na którym zapisuje wykonywane w czasie lekcji ćwiczenia i zadania (w tym te na ocenę).</em></li>
  <li><em style="color: #d50; font-weight: bold;">Uczeń posiada skonfigurowaną skrzynkę pocztową e-mail, za pomocą której wysyła zadania do oceny. Przez skonfigurowaną skrzynkę rozumie się taką, 
    w której nadawca jest podpisany imieniem i nazwiskiem - czyli pozwala jednoznacznie i bez problemu stwierdzić kto jest nadawcą wiadomości.</em></li>
  <li><em style="color: #d50; font-weight: bold;">Uczeń potrafi tworzyć, kopiować, przenosić i usuwać pliki na komputerze.</em></li>
  <li><em style="color: #d50; font-weight: bold;">Uczeń zna często spotykane formaty plików, wie czym jest rozszerzenie nazwy pliku. 
    Potrafi zapisać plik w różnych formatach, odpowiednio nazwać plik - nadając mu właściwe rozszerzenie. Potrafi zmienić nazwę pliku bądź zapisać go pod inną nazwą.</em></li>
  <li><em style="color: #d50; font-weight: bold;">Uczeń nabył inne podstawowe wiadomości i umiejętności w czasie nauki w gimnazjum, 
    w szczególności dotyczące posługiwania się komputerem oraz najważniejszymi aplikacjami.</em></li>
</ol>

<h1>Formy (uzyskania) oceny</h1>
<p>Uczeń uczący się informatyki może zostać oceniony za:</p>
<ol>
  <li>ćwiczenia wykonywane na bieżąco w czasie lekcji;</li>
  <li>kartkówki, czyli krótkie ćwiczenia dotyczące kilku ostatnich lekcji wykonywane na początku lub na końcu zajęć;</li>
  <li>ćwiczenia i zadania w formie projektów wykonywane poza lekcjami (zadania domowe);</li>
  <li>sprawdziany wiadomości i umiejętności;</li>
  <li>aktywność, odpowiedzi podczas lekcji;</li>
  <li>szczególne osiągnięcia w konkursach i olimpiadach informatycznych.</li>
</ol>
<p>Ponadto nauczyciel może wystawić w ciągu roku szkolnego oceny za:
  <ul>
    <li>przygotowanie i przeprowadzenie konkursu klasowego, quizu, gier zabaw dydaktycznych lub aktywny udział w tych formach zajęć,</li>
    <li>za wykonanie ciekawej gazetki ściennej,</li>
    <li>za dokumenty przygotowane (przy pomocy komputera) na potrzeby szkoły,</li>
    <li>za artykuły przygotowane na szkolną stronę internetową.</li>
  </ul>
</p>
<p style="text-decoration: line-through;">Posiadanie podręcznika i zeszytu przedmiotowego na lekcjach informatyki w zakresie podstawowym (klasa pierwsza) jest nieobowiązkowe.</p>

<h1>Sposób oddawania zadań do oceny</h1>
<ol>
  <li>Uczeń ma obowiązek w poprawny sposób zapisać zadanie (właściwa nazwa pliku, odpowiednie miejsce zapisu pliku), a w przypadku zadań domowych w prawidłowy sposób przesłać go do oceny.</li>
  <li>W przypadku zapisania zadania inaczej niż wskazano w samym zadaniu, zapisania go w innym miejscu niż wskazano, przesłania go na niewłaściwy adres lub przesłania go z niepodpisanej skrzynki pocztowej albo w inny sposób niepozwalający na stwierdzenie autora lub wykonanego zadania uczeń otrzymuje ocenę niedostateczny wraz z upływem terminu zadania.</li>
  <li>Za sposób zapisania lub przesłania zadnia uczeń może mieć przyznane lub nieprzyznane punkty.</li>
  <li>Literówki, błędy w nazwie pliku, w temacie wiadomości skutkują nieprzyznaniem punktów za ten element zadania. W przypadku rażących błędów mogą to być punkty ujemne.</li>
</ol>

<h1>Nieprzygotowanie do zajęć</h1>
<ol>
  <li>Uczeń ma prawo dwukrotnie w ciągu roku zgłosić nieprzygotowanie. Nieprzygotowanie obejmuje niezapowiedzianą kartkówkę, ćwiczenia na ocenę w czasie lekcji lub brak zadania domowego.</li>
  <li><em style="color: #d50; font-weight: bold;">Uczeń nie może zgłosić nieprzygotowania w czerwcu, czyli w ostatnich trzech tygodniach nauki.</em></li>
  <li>Nieprzygotowanie nie dotyczy zapowiedzianych sprawdzianów, kartkówek i projektów.</li>
  <li>W przypadku zgłoszenia nieprzygotowania dotyczącego obowiązkowego zadania (w formie kartkówki, ćwiczeń lub zadania domowego) uczeń ma dodatkowe dwa tygodnie na wykonanie zadania.</li>
</ol>

<h1>Terminy zadań</h1>
<ol>
  <li>Informacje o terminie zadań domowych, ćwiczeń na ocenę oraz sprawdzianach będą przekazywane poprzez odpowiednie moduły w dzienniku elektronicznym. Informacja o nich może być również umieszczana na stronie internetowej <a href="www.2lolancut.pl/informatyka">www.2lolancut.pl/informatyka</a>.</li>
  <li>Jeżeli uczeń nie dostarczy obowiązkowego zadania domowego w wymaganym terminie i nie zgłosi nieprzygotowania lub wykorzystał wcześniej limit nieprzygotowań, otrzymuje ocenę niedostateczny.</li>
  <li>Jeżeli uczeń nie przystąpi bez uzasadnionej przyczyny do sprawdzianu lub kartkówki w terminie wyznaczonym w punkcie 1 otrzymuje ocenę niedostateczny.</li>
  <li>W przypadku nieprzystąpienia z różnych przyczyn do obowiązkowego sprawdzianu lub kartkówki w wyznaczonym terminie uczeń ma obowiązek do niego przystąpić w ciągu dwóch tygodni od ustania przyczyny nieprzystąpienia.</li>
  <li>Jeżeli uczeń pomimo upływu czasu o którym mowa w punkcie 4 lub punkcie 3 rozdziału nieprzygotowanie do zajęć uczeń nie przystąpi do zadania lub nie wykona zadania domowego otrzymuje z niego ocenę niedostateczną.</li>
  <li><em style="color: #d50; font-weight: bold;">W przypadku wcześniejszego zgłoszenia nieprzygotowania zadania domowego lub nieprzygotowania do kartkówki uczeń ma dwa tygodnie na jego realizację (patrz punkt 3 poprzedniego paragrafu).
    Jeżeli w ciągu dwóch tygodni uczeń nie zrealizuje tego zadania otrzyma ocenę niedostateczny.</em></li>
</ol>

<h1>Poprawianie ocen</h1>
<ol>
  <li>Uczeń za każde zadanie może być oceniony trzykrotnie, czyli dwukrotnie może poprawiać ocenę.</li>
  <li>Ocena niedostateczny uzyskana za nieprzystąpienie do sprawdzianu, kartkówki lub niezrealizowanie zadania domowego jest liczona jako pierwsze ocenienie zadania.</li>
  <li>Do końcowej oceny w największym stopniu liczy się najwyższa uzyskana ocena z poprawianego zadania. Oceny poprawione też są brane pod uwagę ale mają niższą wagę.</li>
  <li>Uczeń nie może poprawiać zadania jeżeli wcześniej w rażący sposób naruszył prawo autorskie. W przypadku skopiowania bądź plagiatowania pracy otrzymana ocena jest oceną ostateczną.</li>
</ol>

<h1>Waga ocen</h1>
<ol>
  <li>Każda uzyskana przez ucznia ocena ma własną wagę i w innym stopniu wpływa na ocenę końcową.</li>
  <li>Waga ocen jest wyznaczana w zależności od ilości lekcji, które obejmuje ocena, stopnia trudności zadania oraz czasu, który należy przeznaczyć na zadanie.</li>
  <li>Wagę 1 mają oceny dotyczące jednej lekcji (np. aktywność na lekcji, bieżące zadania na lekcji, niektóre kartkówki lub zadania domowe z ostatniej lekcji)</li>
  <li>Wagę 1,5 mają oceny dotyczące 2-3 lekcji.</li>
  <li>Wagę 2 mają oceny dotyczące czterech lub większej ilości lekcji (np. sprawdziany).</li>
  <li>Jeżeli przewidywany czas potrzebny na wykonanie zadania jest dłuższy niż 45 minut, waga oceny może zostać zwiększona o 0,5</li>
  <li>Jeżeli zadanie jest o wyższym stopniu trudności, jego waga może zostać zwiększona o 0,5.</li>
  <li>Wagi dla poszczególnych zadań obowiązkowych będą podawane w dzienniku lub na stronie internetowej <a href="www.2lolancut.pl/informatyka">www.2lolancut.pl/informatyka</a>.</li>
  <li>W przypadku poprawiania oceny za zadanie liczone są wszystkie oceny za to zadanie – patrz punkt 3 rozdziału poprawianie ocen. Suma ich wag stanowi wagę wymienioną w punkcie 8.</li>
  <li>W przypadku niezrealizowania zadania w terminie waga pierwszej oceny za zadanie to minimum 10% ogólnej wagi za zadanie (punkt 8). 
    Waga ta będzie zwiększana o 2,5% za każdy rozpoczęty tydzień opóźnienia w realizacji zadania. Jeżeli uczeń poprawi zadanie po 10 tygodniach lub później,
    waga pierwszej oceny za zadanie będzie wynosić 0,25*wagi za zadanie podanej w punkcie 8.</li>
</ol>

<h1>Punkty w zadaniach</h1>
<p>W przypadku prac ocenianych punktowo wprowadza się następujące przeliczenie punktów uzyskanych za prace pisemne na oceny:
   <ul>
       <li>Poniżej 30 % — niedostateczny</li>
       <li>30 % - 49 % — dopuszczający</li>
       <li>50 % - 69 % — dostateczny</li>
       <li>70 % - 86 % — dobry</li>
       <li>87 % - 97 % — bardzo dobry</li>
       <li>98 % - 100 % — celujący</li>
   </ul>
</p>
<p>W przypadku zadań bardzo łatwych lub bardzo trudnych wyżej podana skala może ulec modyfikacji.</p>

<h1>Inne uwagi</h1>
<p>Uczeń może uzyskać ocenę niedostateczną za brak zadania, niezapisanie zadania lub zapisanie w niewłaściwym miejscu, bądź też za wysłanie jej na niewłaściwy adres mailowy
  lub wpisanie niewłaściwego tematu w mailu, złe nazwanie pliku z zadaniem.</p>
<p>W przypadku stwierdzenia niesamodzielnego wykonania pracy (nie dotyczy prac w grupach) lub jej kopiowania uczniowie, którzy przedstawili taką pracę uzyskują ocenę niedostateczną
  bez prawa jej poprawy. Ocenę niedostateczną uzyskuje też uczeń (bądź uczniowie), którzy taką pracę udostępnili (autorzy pracy).</p>
<p>W przypadku korzystania z innych źródeł niż lekcje i podręcznik należy zgodnie z prawem autorskim przy pracy podać bibliografię bądź informację o autorze i pochodzeniu cytatów,
  zdjęć i innych użytych w pracy materiałów. Wszelkie naruszenia prawa autorskiego powodują obniżenie oceny za zadanie lub ocenę niedostateczną.</p>
<p>Wszystkie sprawdziany, zadania domowe i kartkówki, które zostaną wpisane w e-dzienniku lub na stronie <a href="www.2lolancut.pl/informatyka">www.2lolancut.pl/informatyka</a>
  są obowiązkowe, chyba że przy samym zadaniu zaznaczono inaczej.</p>
<p>Uczeń nieobecny na zajęciach ma obowiązek samodzielnie uzupełnić wiedzę i umiejętności, które były przedstawione w czasie tych zajęć oraz dowiedzieć się o zapowiedzianych zadaniach, 
  kartkówkach i sprawdzianach. Nieobecność nie zwalnia ucznia z obowiązku wykonania obowiązkowych zadań.</p>
