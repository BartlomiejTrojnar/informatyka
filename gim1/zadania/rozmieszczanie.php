     <h1>Co trzeba umieć?</h1>
     <p>Zagadnienia z lekcji 7-9, ale przydatne także będą lekcje 5-6 oraz 3-4.</p>

     <h1>Pliki do zadania</h1>
     <p><a href="pliki/zaproszenie.rtf">zaproszenie.rtf</a> oraz wzór: <a href="pliki/zaproszenie.pdf">zaproszenie.pdf</a></p>
     <p><a href="pliki/wyprawy.rtf">wyprawy.rtf</a> oraz wzór: <a href="pliki/wyprawy.pdf">wyprawy.pdf</a></p>

     <h1>Sposób wykonania</h1>
     <h2>Część I - używanie tabulatorów</h2>
     <p>Pobierz i otwórz pliki <span>1)<a href="pliki/zaproszenie.rtf">zaproszenie.rtf</a> oraz 2)<a href="pliki/zaproszenie.pdf">zaproszenie.pdf</a></span>.</p>
     <p>Sformatuj plik 1) czyli przygotuj zaproszenie na otwarcie restauracji według wzoru z dokumentu <span>zaproszenie.pdf</a></span>.
       Posługuj się sposobami i metodami poznanymi na lekcji. Całą pracę można wykonać przy pomocy formatowania czcionki i akapitu w narzędziach głównych
       oraz poprzez użycie tabulatorów, a zatem posługując się umiejętnościami poznanymi na <a href="../lekcja05">lekcji 5</a>, <a href="../lekcja06">lekcji 6</a> oraz <a href="../lekcja07">lekcji 7</a>.
       <span style="color: blue;">Przy wykonywaniu zadania <strong>nie używaj tabel</strong>.</span>
       Wykonując zadanie zadbaj by wprowadzać <strong>jak najmniej znaków</strong>, również tych „białych” czyli niedrukowanych (<strong>spacje, znaki nowej linii, akapitu, tabulatory itd.</strong>).
       Pamiętaj że za niewłaściwe wielokrotne użycie tych znaków będzie obniżona ocena lub wstawiona ocena niedostateczna.
       W miejsce słów imię i nazwisko wstaw swoje dane, adres <strong>wymyśl</strong>.
      </p>
      <p>Zapisując wykonane zmiany nazwij plik:<br />
        <code>1x_Nazwisko_Imie_zaproszenie.docx</code> - jeżeli używasz edytora tekstu Word;<br />
        <code>1x_Nazwisko_Imie_zaproszenie.doc</code> - jeżeli używasz edytora tekstu Word w wersji 2003 lub starszej;<br />
        <code>1x_Nazwisko_Imie_zaproszenie.odt</code> - jeżeli używasz edytora tekstu Writer z pakietu OpenOffice.<br />
        <strong>Zauważ że zmienia się format pliku czyli jego rozszerzenie!</strong>
      </p>

      <h2>Część II - podział dokumentu na sekcje</h2>
      <p>Pobierz i otwórz pliki <span>1)<a href="pliki/wyprawy.rtf">wyprawy.rtf</a></span> oraz <span>2)<a href="pliki/wyprawy.pdf">wyprawy.pdf</a></span>.</p>
      <p>Sformatuj plik 1) <span>wyprawy.rtf</span> według wzoru z dokumentu 2)<span>wyprawy.pdf</span>.
        Aby tego dokonać podziel dokument na sekcje i dokonaj zmian zwracając uwagę na następujęce rzeczy:
        <ol>
           <li>Tekst w rozdziale II (Chiny, bez tytułu rozdziału) napisany jest w trzech kolumnach (rozdzielone linią, 0,5cm odstępu).</li>
           <li>W rozdziale II (Chiny, bez tytułu rozdziału) zmniejszone marginesy (lewy i prawy) na 1cm.</li>
           <li>W rozdziale I (Wyprawa na Elbrus) oraz III (Gruzja) tekst napisany w jednej kolumnie, marginesy po 2,5cm.</li>
           <li>W każdym rozdziale ustawiony nagłówek strony - tytuł rozdziału (wyrównany do prawej, powiększony, pochylony oraz linia pod nim).</li>
           <li>W każdym rozdziale również brak nagłówka (albo pusty nagłówek) na pierwszej stronie.</li>
           <li>W każdym rozdziale numery na środku stopki strony (oprócz pierwszych stron).</li>
         </ol>
      </p>
      <p>Zapisując wykonane zmiany nazwij plik:<br />
        <code>1x_Nazwisko_Imie_wyprawy.docx</code> - jeżeli używasz edytora tekstu Word;<br />
        <code>1x_Nazwisko_Imie_wyprawy.doc</code> - jeżeli używasz edytora tekstu Word w wersji 2003 lub starszej;<br />
        <code>1x_Nazwisko_Imie_wyprawy.odt</code> - jeżeli używasz edytora tekstu Writer z pakietu OpenOffice.<br />
        <strong>Zauważ że zmienia się format pliku czyli jego rozszerzenie!</strong>
      </p>

      <h1>Wysyłanie zadania</h1>
      <h2>Uwagi</h2>
      <p>Nazwy plików mają być dokładnie jak podano w treści zadania.</p>
      <p>Informatyka to przedmiot ścisły. Bądź precyzyjny, każda spacja, kropka, literka jest ważnym znakiem, więc <strong>nazwy wpisuj dokładnie jak polecono!</strong></p>
      <p>Przed wysłaniem wiadomości sprawdź czy wykonałeś(-aś) polecenie z lekcji 3 dotyczące ustawiania pola OD dla Twojej skrzynki pocztowej.</p>
      <p><strong>Zadania wysłane za pomocą niepodpisanego konta pocztowego, z niewłaściwym tematem lub z niewłaściwie nazwanym
         plikiem nie będą oceniane lub zostaną przy nich obniżone oceny! Przed wysłaniem zadania sprawdź czy nie masz literówek w nazwie pliku, polu OD, temacie a także w adresie odbiorcy.</strong>
      </p>
      <p>Nie wysyłaj zadania w częściach. W tym zadaniu wysyłasz jednocześnie dwa pliki do oceny.
         Oceniany będzie ostatni wysłany mail, dlatego jeżeli się pomyliłeś nie wahaj się wysłać poprawionego zadania jeszcze raz.
      </p>

      <h2>Wysyłanie</h2>
