         <li>Wpisz temat wiadomości: <code>1x rozmieszczanie tekstu</code> (w miejsce <code>x</code> wpisz symbol klasy)</li>
         <li>Wyślij wiadomość do: <code>bartek_tr@tlen.pl</code></li>
      </ol>

