    <h1>Uwagi</h1>
    <p>Nazwy plików mają być dokładnie jak podano niżej.
      Jeżeli w domu posiadasz starszą wersję Office’a to zamiast rozszerzenia .docx, zapisz plik z rozszerzeniem .doc.
      Jeżeli posiadasz pakiet OpenOffice to rozszerzeniem pliku będzie .odt.</p>
    <p>Informatyka to przedmiot ścisły. Bądź precyzyjny, każda spacja, kropka, literka jest ważnym znakiem, więc nazwy wpisuj dokładnie jak polecono!</p>
    <p>O nazywaniu i zapisywaniu plików możesz przeczytać w lekcji 3: <a href="/informatyka/gim1/lekcja03/">Rozszerzenia nazw plików</a>.</p>

    <h1>Przygotowanie</h1>
    <p>Utwórz nowy plik edytora tekstu i nazwij go następująco:<br />
    jeżeli używasz Microsoft Word 2007 lub nowszego: <code>1x_Nazwisko_Imie_komunikacja.docx</code>;<br />
    jeżeli używasz Microsoft Word 2003 lub starszego: <code>1x_Nazwisko_Imie_komunikacja.doc</code>;<br />
    jeżeli używasz Open Office lub LibreOffice: <code>1x_Nazwisko_Imie_komunikacja.odt</code><br />
    gdzie w miejsce <em>x</em> wpisz symbol klasy (a,b,c,d,e lub f) a zamiast <em>Nazwisko_Imie</em> wpisz swoje nazwisko i imię (bez zdrobnień). Zwróć uwagę kropki, spacje i inne znaki w nazwie.
    <br /><span>5 punktów</span></p>

    <h1>Komunikacja w sieci</h1>
    <p>W pliku opisz krótko (jedno-dwa zdania) czym jest komunikacja synchroniczna i asynchroniczna (między osobami). <span>4 punkty</span></p>
    <p>Napisz również czym się różni odbiorca pasywny i aktywny. <span>4 punkty</span></p>
    <p>Skorzystaj z wiadomości poznanych na lekcjach lub w podręczniku. Możesz również zajrzeć na lekcję 4: <a href="/informatyka/gim1/lekcja04/">Komunikacja i wymiana zasobów informacyjnych</a>.</p>

    <h1>Konfiguracja poczty</h1>
    <p>Skonfiguruj własną pocztę e-mail w taki sposób, aby w polu <em>OD</em> pojawiało się Twoje imię i nazwisko. <span>3 punkty</span></p>
    <p>Ustaw podpis (sygnaturę) do każdej wysyłanej wiadomości, coś w stylu:
      <span class="przyklad r">Pozdrawiam<br />Jan Kowalski</span> <span>1 punkt</span></p>

