    <h1>Co trzeba umieć?</h1>
    <p>Wprowadzanie znaków w edytorze tekstu (Word). Wprowadzanie znaków specjalnych oraz niedrukowanych (twarda spacja, nowa strona, nowa linia).
      <br />Zaznaczanie fragmentów tekstu: zaznaczanie słów, linii, akapitów, całego dokumentu i innch fragmentów za pomocą myszy i klawiatury.
      <br />Formatowanie czcionki w edytorze tekstu, w tym opcje zaawansowane.
      <br />Formatowanie akapitu: wyrównanie, wcięcia (lewa, prawa), wcięcia specjalne, odstęp przed i po akapicie, interlinia.
      <br />Kopiowanie fragmentów tekstu, kopiowanie formatu tekstu (malarz formatów).</p>
    <p>Zagadnienia te są opisane na <a href="../lekcja05">lekcji 5</a> i <a href="../lekcja06">lekcji 6</a>.</p>

