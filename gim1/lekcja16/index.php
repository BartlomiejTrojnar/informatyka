    <h1>Bezpieczeństwo pracy przy komputerze</h1>
    <h2>Zagrożenie wirusem, koniem trojańskim, robakiem itp.</h2>
    <p>W celu zabezpieczenia naszego komputera:</p>
    <ol>
      <li>aktualizacja systemu operacyjnego</li>
      <li>aktualizacja używanych programów</li>
      <li>zainstalowanie i włączenie programu antywirusowego</li>
      <li>zainstalowanie i włączenie firewalla (może być częśćią antywirusa)</li>
    </ol>

    <h2>Nasze błędy</h2>
    <p>Nawet najlepsze antywirusy nie ochronią nas przed naszą bezmyślnością i brakiem rozsądku.</p>
    <p>Po czym poznać że strona (banku) jest bezpieczna? Kłódka przy adresie, zweryfikowana tożsamość strony, zaszyfrowane połączenie protokołem https.</p>
    <p>Emaile z prośbą o weryfikację - od banku? Brak danych osobowych, napisane niepoprawną polszczyzną, bez polskich znaków diakrytycznych. Przekierowanie pod zły adres, na którym strona wygląda identycznie jak oryginalna.</p>
    <p>Zapamiętywanie haseł w przeglądarce? Szybkie uzyskanie haseł w miejscach publicznych. Stare strony internetowe - niezabezpieczone bazy danych z hasłami. Te same hasła na różne strony. Propozycja różnych haseł do różnych witryn. A może stosowanie menedżera haseł?</p>

    <h2>Zagrożenia w Internecie</h2>
    <p>Niektórzy łamią porządek prawny z założenia. Uciekają się do różnych sztuczek byle tylko uzyskać od nas pieniądze.</p>
    <p>Anonimowość w Internecie. My zawsze pozostawiamy ślady, ale czy oszust nie będzie potrafił ich zatrzeć, pomieszać tak aby praktycznie niemożliwe było znalezienie go?</p>
    <p>Instalowanie programów i dodatków niewiadomego pochodzenia - zwiększenie zagrożenia.</p>

    <h2>Ciasteczka</h2>
    <p>Po co są pliki cookies?</p>
    <ol>
      <li>personalizacja wyglądu serwisu</li>
      <li>wykorzystywane do zapamiętywania czynności wykonywanych przez użytkownika</li>
      <li>w celach statystycznych</li>
      <li>zapamiętują różne informacje o nszych działaniach, preferencjach itp.</li>
    </ol>

