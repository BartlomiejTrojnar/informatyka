<h1>Zasady prezentacji</h1>
<p>Prezentacja powinna zawierać sprawdzone i dokładne informacje</p>
Prezentacja nie może zawierać błędów ortograficznych ani gramatycznych
Struktura kolejnych slajdów powinna być przejrzysta i czytelna
Nie zostawiamy spójników i przyimków (a, i, o, u ,w , z ) na końcu wiersza –ustawiamy kursor przed spójnikiem i naciskamy klawisze Shift+Enter
Elementy graficzne dodane do slajdów powinny uatrakcyjniać wygląd slajdu , a nie utrudniać odbioru treści .
Wykorzystane przyciski powinny pomagać w prawidłowym sterowaniu prezentacją
Slajdy powinny mieć jednolitą szatę graficzną, czyli tło, kolory, style napisów, rodzaje stosowanych wyróżnień i podkreśleń.
Bardzo ważna jest estetyka- kolory tła i liter powinny harmonizować z pozostałymi elementami slajdu: należy z umiarem używać kolorów i pamiętać, że ich niewłaściwy dobór znacznie utrudni oglądanie prezentacji.
Dodawane efekty multimedialne ( takie jak przejścia slajdów czy dźwięk ) powinny być tak dobrane, aby uatrakcyjnić prezentację, a nie drażnić czy wręcz niepokoić odbiorców.
Wykorzystywane materiały w prezentacji mają swoich autorów, musisz zatem pamiętać o podaniu nazwiska autora, tytułu cytowanego utworu, wydawnictwa, roku wydania, adresu strony internetowej itp. (Bibliografia)
Prezentacja powinna zawierać dane autora.
