  <h1>Prezentacja - projekt na ocenę</h1>
  <p>Twoim zadaniem jest przygotowanie występu i przedstawienie prezentacji w terminie określonym w czasie lekcji.</p>
  <h2>Wymagania</h2>
  <p>Prezentację można przygotować samodzielnie lub w grupie dwuosobowej lub trzyosobowej. Grupa większa nie będzie dopuszczona do przedstawiania.</p>
  <ul>
    <li>Temat prezentacji: dowolny.</li>
    <li>Czas wystąpienia:
      <ul>
        <li>prezentacja jednoosobowa - 4-5 minut,</li>
        <li>prezentacja dwuosobowa - 6-7 minut,</li>
        <li>prezentacja trzyosobowa - 8-10 minut.</li>
      </ul>
    </li>
    <li>W prezentacji można użyć/pokazać przedmioty/plansze/osoby realnie (nie tylko na komputerze).</li>
    <li>W prezentacji należy użyć chociaż krótko komputera (np. część lub całość prezentacji w postaci slajdów). Część komputerową można przygotować np w postaci pokazu slajdów PowerPoint, prezentacji Prezi, plików pdf, zdjęć i innych.</li>
    <li>Do prezentacji należy dołączyć podkład muzyczny lub krótki film. Plik muzyczny może być "tłem" prezentacji.</li>
    <li>W prezentacji należy ująć autorów i tytuł prezentacji, a także podsumowanie wystąpienia oraz źródła/bibliografię, które zostały wykorzystane do przygotowania prezentacji.</li>
    <li>Oceniane będą: spójność, jednolitość, przejrzystość i czytelność przygotowanych materiałów. Czyli należy unikać stosowania zbyt wielu czcionek, kolorów, stylów w prezentacji, chyba że jest to uzasadnione tematem prezentacji. Na slajdzie powinno być tyle tekstu, by dało się go przeczytać - duża wielkość liter, unikać więcej niż 8 wierszy tekstu na slajdzie. Należy zadbać o dobry kontrast tekstu z tłem - slajdy wyświetlane przy użyciu projektora są zazwyczaj mniej wyraźne.</li>
    <li>Należy zadbać o ortografię, gramatykę i interpunkcję. Poprawić literówki, wstawiać pojedyncze spacje między literami, kończyć zdania kropką. Przed kropką, przecinkiem, średnikiem nie występuje spacja, a jest ona po tych znakach. Przy stosowaniu myślników i pauz spacje są po obu stronach znaku, itp.</li>
    <li>Nie zostawiamy spójników i przyimków (a, i, o, u, w, z) na końcu wiersza.</li>
    <li>Wykorzystane materiały mają swoich autorów, należy zatem podać nazwisko autora, tytuł cytowanego utworu, wydawnictwo, rok wydania lub adres strony internetowej z której pobrane zostały materiały wraz z datą jej publikacji.</li>
    <li>Dodatkowo punktowane będą: odpowiednie wykorzystanie animacji, przejść między slajdami, przycisków do sterowania prezentacją.</li>
  </ul>
